package io.github.natanimn.types.media_and_service;

/**
 * Describes the birthdate of a user.
 * @author Natanim 
 * @since 3 March 2025
 * @version 0.8
 */
public class Birthdate {
    /**
     * Day of the user's birth; 1-31
     */
    public Byte day;

    /**
     * Month of the user's birth; 1-12
     */
    public Byte month;

    /**
     * Optional. Year of the user's birth
     */
    public Integer year;

}
